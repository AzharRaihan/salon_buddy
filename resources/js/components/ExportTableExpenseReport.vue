<template>
  <VMenu>
    <template #activator="{ props }">
      <VBtn 
        v-bind="props"
        variant="tonal" 
        color="secondary" 
        prepend-icon="tabler-upload"
        :loading="isExporting"
      >
        Export
        <VIcon icon="tabler-chevron-down" class="ml-1" />
      </VBtn>
    </template>

    <VList>
      <VListItem
        prepend-icon="tabler-file-type-pdf"
        title="Export as PDF"
        @click="exportToPDF"
      />
      <VListItem
        prepend-icon="tabler-file-type-csv"
        title="Export as CSV"
        @click="exportToCSV"
      />
      <VListItem
        prepend-icon="tabler-file-spreadsheet"
        title="Export as Excel"
        @click="exportToExcel"
      />
    </VList>
  </VMenu>
</template>

<script setup>
import { ref } from 'vue'
import { toast } from 'vue3-toastify'
import jsPDF from 'jspdf'
import autoTable from 'jspdf-autotable'

const props = defineProps({
  data: {
    type: Array,
    required: true,
    default: () => []
  },
  headers: {
    type: Array,
    required: true,
    default: () => []
  },
  filename: {
    type: String,
    default: 'export'
  },
  title: {
    type: String,
    default: 'Export Data'
  },
  headerData: {
    type: Object,
    default: () => ({
      reportTitle: 'Expense Report',
      outletName: 'All Outlets',
      phone: null,
      address: null,
      responsiblePersonName: null,
      responsiblePersonPhone: null,
      categoryName: null,
      dateRange: 'N/A',
      generatedOn: '',
      generatedBy: 'N/A'
    })
  },
  summaryData: {
    type: Object,
    default: null
  }
})

const isExporting = ref(false)

// Helper function to get nested property value
const getNestedValue = (obj, path) => {
  return path.split('.').reduce((o, p) => o && o[p], obj)
}

// Helper function to format data for export
const formatDataForExport = () => {
  const exportHeaders = props.headers
    .filter(header => header.key !== 'actions')
    .map(header => header.title)

  const exportData = props.data.map(item => {
    const row = {}
    props.headers.forEach(header => {
      if (header.key !== 'actions') {
        let value = getNestedValue(item, header.key)
        
        // Handle special formatting
        if (header.key === 'date' && value) {
          value = new Date(value).toLocaleDateString()
        } else if (header.key.includes('employee') && typeof value === 'object' && value?.name) {
          value = value.name
        } else if (value === null || value === undefined) {
          value = 'N/A'
        }
        
        row[header.title] = value
      }
    })
    return row
  })

  // Add summary row if summaryData is provided
  if (props.summaryData) {
    const summaryRow = {}
    props.headers.forEach(header => {
      if (header.key != 'actions') {
        if (header.key == 'employee.name') {
          summaryRow[header.title] = header.key == 'employee.name' ? 'Total:' : ''
        } else if (header.key == 'amount') {
          summaryRow[header.title] = props.summaryData?.totalAmount || 0
        } else {
          summaryRow[header.title] = ''
        }
      }
    })
    exportData.push(summaryRow)
  }

  return { headers: exportHeaders, data: exportData }
}

// Export to CSV
const exportToCSV = () => {
  try {
    isExporting.value = true
    const { headers, data } = formatDataForExport()

    // Create header info rows
    const headerInfo = [
      props.headerData.reportTitle,
      '',
      `Outlet: ${props.headerData.outletName}`
    ]
    
    if (props.headerData.phone) {
      headerInfo.push(`Phone: ${props.headerData.phone}`)
    }
    if (props.headerData.address) {
      headerInfo.push(`Address: ${props.headerData.address}`)
    }
    if (props.headerData.responsiblePersonName) {
      headerInfo.push(`Responsible Person: ${props.headerData.responsiblePersonName}`)
    }
    if (props.headerData.responsiblePersonPhone) {
      headerInfo.push(`Phone: ${props.headerData.responsiblePersonPhone}`)
    }
    if (props.headerData.categoryName) {
      headerInfo.push(`Category: ${props.headerData.categoryName}`)
    }
    
    headerInfo.push(
      `Date Range: ${props.headerData.dateRange || 'N/A'}`,
      `Generated On: ${props.headerData.generatedOn}`,
      `Generated By: ${props.headerData.generatedBy}`,
      '',
      ''
    )

    const csvContent = [
      ...headerInfo,
      headers.join(','),
      ...data.map(row => 
        headers.map(header => {
          const value = row[header] || ''
          // Escape quotes and wrap in quotes if contains comma
          return typeof value === 'string' && (value.includes(',') || value.includes('"'))
            ? `"${value.replace(/"/g, '""')}"`
            : value
        }).join(',')
      )
    ].join('\n')
    
    const blob = new Blob([csvContent], { type: 'text/csv;charset=utf-8;' })
    const link = document.createElement('a')
    const url = URL.createObjectURL(blob)
    link.setAttribute('href', url)
    link.setAttribute('download', `${props.filename}.csv`)
    link.style.visibility = 'hidden'
    document.body.appendChild(link)
    link.click()
    document.body.removeChild(link)
    
    toast('CSV exported successfully', { type: 'success' })
  } catch (error) {
    console.error('Error exporting CSV:', error)
    toast('Failed to export CSV', { type: 'error' })
  } finally {
    isExporting.value = false
  }
}

// Export to Excel
const exportToExcel = async () => {
  try {
    isExporting.value = true
    
    // Dynamic import to avoid bundling if not used
    const XLSX = await import('xlsx')
    
    const { headers, data } = formatDataForExport()
    
    // Create header info rows
    const headerInfo = [
      [props.headerData.reportTitle],
      [],
      ['Outlet:', props.headerData.outletName]
    ]
    
    if (props.headerData.phone) {
      headerInfo.push(['Phone:', props.headerData.phone])
    }
    if (props.headerData.address) {
      headerInfo.push(['Address:', props.headerData.address])
    }
    if (props.headerData.responsiblePersonName) {
      headerInfo.push(['Responsible Person:', props.headerData.responsiblePersonName])
    }
    if (props.headerData.responsiblePersonPhone) {
      headerInfo.push(['Phone:', props.headerData.responsiblePersonPhone])
    }
    if (props.headerData.categoryName) {
      headerInfo.push(['Category:', props.headerData.categoryName])
    }
    
    headerInfo.push(
      ['Date Range:', props.headerData.dateRange || 'N/A'],
      ['Generated On:', props.headerData.generatedOn],
      ['Generated By:', props.headerData.generatedBy],
      []
    )
    
    // Create worksheet with header info
    const ws = XLSX.utils.aoa_to_sheet(headerInfo)
    
    // Add table headers and data
    XLSX.utils.sheet_add_json(ws, data, { 
      origin: `A${headerInfo.length + 1}`,
      skipHeader: false 
    })
    
    const wb = XLSX.utils.book_new()
    XLSX.utils.book_append_sheet(wb, ws, 'Sheet1')
    XLSX.writeFile(wb, `${props.filename}.xlsx`)
    toast('Excel file exported successfully', { type: 'success' })
  } catch (error) {
    console.error('Error exporting Excel:', error)
    toast('Failed to export Excel file. Please install xlsx package.', { type: 'error' })
  } finally {
    isExporting.value = false
  }
}

// Export to PDF
const exportToPDF = () => {
  try {
    isExporting.value = true

    const { headers, data } = formatDataForExport()
    const doc = new jsPDF()
    const pageWidth = doc.internal.pageSize.getWidth()
    
    let currentY = 15
    
    // Report Title
    doc.setFontSize(16)
    doc.setFont(undefined, 'bold')
    doc.text(props.headerData.reportTitle, pageWidth / 2, 15, { align: 'center' })
    currentY += 10
    
    // Outlet Info (Left Side)
    doc.setFontSize(10)
    doc.setFont(undefined, 'normal')
    const leftStartY = currentY
    doc.text(`Outlet: ${props.headerData.outletName}`, 14, currentY)
    currentY += 6
    
    if (props.headerData.phone) {
      doc.text(`Phone: ${props.headerData.phone}`, 14, currentY)
      currentY += 6
    }
    if (props.headerData.address) {
      doc.text(`Address: ${props.headerData.address}`, 14, currentY)
      currentY += 6
    }
    if (props.headerData.responsiblePersonName) {
      doc.text(`Responsible Person: ${props.headerData.responsiblePersonName}`, 14, currentY)
      currentY += 6
    }
    if (props.headerData.responsiblePersonPhone) {
      doc.text(`Phone: ${props.headerData.responsiblePersonPhone}`, 14, currentY)
      currentY += 6
    }
    if (props.headerData.categoryName) {
      doc.text(`Category: ${props.headerData.categoryName}`, 14, currentY)
      currentY += 6
    }
    
    
    // Generated Info (Right Side)
    doc.text(`Date Range: ${props.headerData.dateRange || 'N/A'}`, pageWidth - 14, leftStartY, { align: 'right' })
    doc.text(`Generated On: ${props.headerData.generatedOn}`, pageWidth - 14, leftStartY + 6, { align: 'right' })
    doc.text(`Generated By: ${props.headerData.generatedBy}`, pageWidth - 14, leftStartY + 12, { align: 'right' })
    
    // Calculate the maximum Y position from both left and right sides
    const rightEndY = leftStartY + 18
    const maxY = Math.max(currentY, rightEndY)
    
    // Add spacing before table (ensure there's enough gap)
    currentY = maxY + 10
    
    // Add table
    autoTable(doc, {
      head: [headers],
      body: data.map(row => headers.map(header => row[header] || '')),
      startY: currentY,
      styles: {
        fontSize: 8,
        cellPadding: 2
      },
      headStyles: {
        fillColor: [66, 139, 202],
        textColor: 255,
        fontStyle: 'bold'
      }
    })

    doc.save(`${props.filename}.pdf`)
    toast('PDF exported successfully', { type: 'success' })
  } catch (error) {
    console.error('Error exporting PDF:', error)
    toast('Failed to export PDF. Please install jspdf and jspdf-autotable packages.', { type: 'error' })
  } finally {
    isExporting.value = false
  }
}
</script> 
